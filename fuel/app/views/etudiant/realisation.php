<h2>Conseils spécifiques aux étudiants</h2>

<h3>Attitude et intégration dans l'entreprise</h3>

<p>
	Chaque entreprise a ses codes et ses habitudes de vie : certaines seront très strictes sur la tenue vestimentaire ou les horaires, d'autres moins.
	Dans tous les cas votre rôle de stagiaire est de vous intégrer et de vous plier à ces habitudes. Méfiez-vous en particulier des groupes qui s'affichent très cool
	mais qui ont en fait des codes cachés que vous pourriez découvrir plus (trop) tard à vos dépends ; le plus sûr est d'adopter une attitude sérieuse et réservée en attendant de voir.
</p>
<p>Voir à ce sujet la <a href="https://extranet.iut-info-vannes.net/_media/wiki/documents/stages/video_stage_gea_2007.avi">video GEA 2007</a> (attention 131Mo).</p>

<h3>Vos tâches durant le stage</h3>

<p>Pendant le stage, l'essentiel de votre temps sera consacré aux tâche que vous confiera votre maître de stage en entreprise ; cependant l'IUT attend également de vous un certain travail :</p>
<ul>
    <li>un rapport de début de stage à la fin de la première quinzaine,</li>
    <li>un compte-rendu régulier de vos activités à votre tuteur enseignant,</li>
    <li>l'organisation de la visite de stage par ce même tuteur,</li>
    <li>la rédaction et le rendu du rapport de stage en fin de stage,</li>
</ul>
<p>
	Suivant les entreprises, ce travail est considéré comme faisant ou non partie du stage : clarifiez la situation dès le début du stage avec votre maître de stage,
	ceci évitera les mauvaises surprises à la fin. Si l'entreprise considère que les tâches académiques ne font pas partie du stage, vous devrez les faire en dehors de votre 
	temps de travail dans l'entreprise.
</p>
<h3>Rendus et suivi de stage</h3>
<p>Pour permettre un suivi simplifié du stage et un rendu facile à faire et à contrôler, une zone de rendu a été aménagée sur l'extranet, il s'agit d'un espace wiki personnel pour chaque étudiant.</p>
<h4>CADRE</h4>
<p>
Espaces de suivi de stage<br />
Sur le serveur de projets<br />
http://projets.iut-info-vannes.net/wiki/rendus/etud/<votre_login>/stage/
</p>
<p>Les documents de rapport doivent y être déposés au format PDF uniquement, avec des liens qui donnent un accès direct dessus.</p>
<p>
	Vous pouvez en outre organiser votre espace Wiki pour gérer les comptes-rendus d'avancement directement dessus ;
	les enseignants tuteurs ont un accès direct sur votre zone de rendu depuis leur fiche de suivi. Si vous souhaitez conjointement avec votre maître de stage faire de cette zone
	un espace collaboratif entre vous, le maître de stage et le tuteur, il suffit de demander l'enregistrement de votre maître de stage comme usager sur le serveur de projets. 
</p>
<h3>Contacts avec le professeur-tuteur</h3>
<p>Dès le début de stage, prenez l'habitude de transmettre régulièrement de vos nouvelles à votre professeur-tuteur. C'est à vous de prendre l'initiative des contacts.</p>
<h4>CADRE</h4>
<p>N'attendez pas systématiquement une réponse : elle est rarement nécessaire. Ainsi, n'harcelez pas votre professeur-tuteur, cela sera interprété comme un manque d'autonomie et de maturité professionnelle.</p>