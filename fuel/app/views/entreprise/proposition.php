		<h2>Proposer un sujet de stage</h2><hr/>
		<h3>Principes</h3><hr />
		<ol>
			<li>Ce sont les étudiants qui gèrent de bout en bout leur «embauche de stage» au même titre qu'une embauche classique ; ils doivent avoir l'initiative de négocier avec vous le contenu et les conditions du stage, les enseignants du Département Informatique n'interviennent pas dans ce processus.</li>
			<li>Les offres que vous nous transmettez sont présentées aux étudiants sur notre extranet après validation par l'enseignant coordinateur des stages.</li>
			<li>Plusieurs étudiants peuvent vous contacter pour le même stage.</li>
			<li>Une fois un accord établi entre l'entreprise et le stagiaire, celui-ci doit remplir une fiche de description détaillée du stage qui va permettre la validation finale du stage par l'équipe enseignante ; après quoi la convention de stage est établie et signée.</li>
		</ol>
		<h3>Protocole</h3><hr/>
		<p>
			Pour proposer un stage, cliquer le bouton en bas de page (vous pouvez au préalable consulter <a href="./liste">la liste des propositions déjà saisies</a>)
		</p>
		<p>Tous les champs doivent être renseignés, en particulier l'adresse de courriel doit être réelle et complète, sans quoi nous ne pourrons pas vous envoyer d'acquittement ni vous re-contacter par la suite.</p>
		<p>Un courriel en retour vous indique que votre demande a bien été enregistrée, le coordonnateur vous informera ensuite de la validation de votre proposition (mise à disposition des étudiants).</p>
		<p>Quand vous aurez conclu avec un étudiant, celui-ci devra nous avertir au plus tôt de cette décision afin que nous puissions retirer votre offre de l'affichage et donc vous éviter le dérangement de candidatures inutiles. En fait, il ne peut y avoir de candidatures multiples que pendant la phase de négociation.</p>
		<p>Pour la suite des évènements, reportez-vous à la page d'accueil.</p>
		
